namespace System.Windows.Media
{
    /// <summary>
    /// Specifies whether text rendering is optimized for readability or for dynamic
    /// presentation.
    /// </summary>
    public enum TextHintingMode
	{
        /// <summary>
        /// The default text render mode.
        /// </summary>
        Fixed = 0,
        /// <summary>
        /// Turns off text rendering optimizations.
        /// </summary>
        Animated = 1
	}
}
