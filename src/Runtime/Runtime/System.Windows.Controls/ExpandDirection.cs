namespace System.Windows.Controls
{
    /// <summary>
    /// Specifies the direction in which an <see cref="Expander" /> control opens.
    /// </summary>
    public enum ExpandDirection
    {
        /// <summary>
        /// Specifies that the <see cref="Expander" /> control opens in the down direction.
        /// </summary>
        Down = 0,

        /// <summary>
        /// Specifies that the <see cref="Expander" /> control opens in the up direction.
        /// </summary>
        Up = 1,

        /// <summary>
        /// Specifies that the <see cref="Expander" /> control opens in the left direction.
        /// </summary>
        Left = 2,

        /// <summary>
        /// Specifies that the <see cref="Expander" /> control opens in the right direction.
        /// </summary>
        Right = 3
    }
}
